<?php

namespace Untek\Utility\Log\Domain\Services;

use Untek\Utility\Log\Domain\Entities\HistoryEntity;
use Untek\Utility\Log\Domain\Entities\LogEntity;
use Untek\Utility\Log\Domain\Interfaces\Repositories\HistoryRepositoryInterface;
use Untek\Utility\Log\Domain\Interfaces\Services\HistoryServiceInterface;
use Untek\Domain\Service\Base\BaseCrudService;
use Untek\Domain\EntityManager\Interfaces\EntityManagerInterface;
use Untek\Domain\DataProvider\Libs\DataProvider;
use Untek\Domain\Query\Entities\Query;

class HistoryService extends BaseCrudService implements HistoryServiceInterface
{

    public function __construct(EntityManagerInterface $em, HistoryRepositoryInterface $historyRepository)
    {
        $this->setEntityManager($em);
        $this->setRepository($historyRepository);
    }

    public function getEntityClass(): string
    {
        return HistoryEntity::class;
    }

    public function getDataProvider(Query $query = null): DataProvider
    {
        return parent::getDataProvider($query); // TODO: Change the autogenerated stub
    }
}
